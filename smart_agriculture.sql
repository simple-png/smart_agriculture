(
    id            int primary key auto_increment comment '主键',
    category_id   int comment '种类id',
    name          varchar(32) comment '周期名字',
    cycle_percent varchar(32) comment '在整个周期的占比:如1-2',
    soil_moisture varchar(32) comment '这个周期需要的土壤湿度:如22.33'
);
